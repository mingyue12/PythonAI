# 算数运算符
print("5 + 2 =", 5 + 2)  # 加法
print("5 - 2 =", 5 - 2)  # 减法
print("5 * 2 =", 5 * 2)  # 乘法
print("5 / 2 =", 5 / 2)  # 除法
print("5 // 2 =", 5 // 2)  # 整除
print("5 % 2 =", 5 % 2)  # 取余
print("2 ** 3 =", 2 ** 3)  # 幂运算

# 赋值运算符=
age = 18
age = age + 5

# 复合赋值运算符
age += 5  # age = age + 5
age -= 5  # age = age - 5
age *= 5  # age = age * 5
age /= 5  # age = age / 5
age //= 5  # age = age // 5
age %= 5  # age = age % 5
age **= 5  # age = age ** 5

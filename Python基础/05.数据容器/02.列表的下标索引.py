"""
语法：
列表[下标索引值]
"""

name_list = ["周杰伦", "林俊杰", "王力宏"]
print(name_list[0])  # 取出第一个元素，下标是[0]
print(name_list[1])

# 反向下标从右到左
lst = [10, 20, 30, 40, 50]
print(lst[-1])  # 取出最后一个元素
print(lst[-2])  # 取出倒数第二个元素


# 嵌套列表用法
lst = [[1, 2, 3], ["a", "b", "c"], [True, False]]
print(lst[1][1])  # 取出第二个列表中的第二个元素 "b"



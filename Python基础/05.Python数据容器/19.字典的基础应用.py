"""
语法
{元素, 元素, ...}
元素必须是：key:value 对,称之为键值对
key可以是任意类型（不可以是数据容器）
value可以是任意类型
无法用下标索引，只能用key值来访问value值
"""

# 空字典
d = {}
print(type(d))
d1 = dict()
print(type(d1))

# 字面量
d2 = {"周杰伦": 99, "王力宏": 88, "林俊杰": 66}
print(d2)

# 访问字典中的元素
print(d2["周杰伦"])

# 字典的key是不可以重复的
d3 = {"周杰伦": 99, "王力宏": 88, "林俊杰": 66, "周杰伦": 100}
print(d3)  # 后面的key会覆盖前面的key


"""
带有传入参数的函数
语法：
def 函数名(参数1,参数2,...):
    函数体
    return 返回值
"""


# 函数需求，实现一个两个数字相加函数
def add(a, b):
    """
    这是一个简单的函数，用于计算两个数字的和
    函数名：add
    参数：a, b
    返回值：a + b
    """
    return a + b


# 调用函数
result1 = add(1, 2)
result2 = add("666", "777")
print(result1)
print(result2)

